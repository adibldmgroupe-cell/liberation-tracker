-- ─── 5. VUES UTILES ───────────────────────────────────────────────────

-- Vue : flux complet d'un produit avec détails atelier
CREATE OR REPLACE VIEW v_product_flux_detail AS
SELECT
  pf.product_code,
  pf.product_name,
  pf.route,
  pf.op_number,
  om.op_code,
  om.processus,
  COALESCE(pf.room_code, om.room_code) AS room_code,
  om.room_name,
  om.equipment_name
FROM product_flux pf
JOIN operations_master om ON pf.op_number = om.op_number
ORDER BY pf.product_code, pf.route, pf.op_number;

-- Vue : résumé par produit (type de flux détecté automatiquement)
CREATE OR REPLACE VIEW v_product_flux_summary AS
SELECT
  product_code,
  product_name,
  route,
  COUNT(*) AS nb_etapes,
  CASE
    WHEN bool_or(op_number = 260) THEN 'GLE'
    WHEN bool_or(op_number = 270) THEN 'CREME_POMMADE'
    WHEN bool_or(op_number = 250) THEN 'COM_PELLI'
    WHEN bool_or(op_number = 240) THEN 'COM_SEC'
    ELSE 'OTC'
  END AS type_flux,
  bool_or(op_number = 220) AS has_granulation,
  bool_or(op_number = 230) AS has_melange,
  MIN(CASE WHEN op_number BETWEEN 310 AND 390 THEN op_number END) AS cond_op_number
FROM product_flux
GROUP BY product_code, product_name, route;

