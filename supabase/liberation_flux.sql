-- ─── 1. TABLES ──────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS operations_master (
  id            serial PRIMARY KEY,
  processus     text NOT NULL,
  room_code     text NOT NULL,
  room_name     text NOT NULL,
  op_number     int  NOT NULL,
  op_code       text NOT NULL,
  equipment_name text NOT NULL,
  created_at    timestamptz DEFAULT now()
);

CREATE TABLE IF NOT EXISTS product_flux (
  id            serial PRIMARY KEY,
  product_code  text NOT NULL,
  product_name  text NOT NULL,
  op_number     int  NOT NULL,
  route         int  NOT NULL DEFAULT 1,
  room_code     text,  -- NULL = flexible, valeur = machine fixée
  created_at    timestamptz DEFAULT now()
);

CREATE TABLE IF NOT EXISTS equipment_cadences (
  id                   serial PRIMARY KEY,
  room_code            text NOT NULL,
  product_code         text NOT NULL,
  cadence_theorique    numeric,
  unite                text DEFAULT 'cp/h',
  notes                text,
  created_at           timestamptz DEFAULT now(),
  UNIQUE(room_code, product_code)
);

-- Index utiles
CREATE INDEX IF NOT EXISTS idx_product_flux_code    ON product_flux(product_code);
CREATE INDEX IF NOT EXISTS idx_product_flux_op      ON product_flux(op_number);
CREATE INDEX IF NOT EXISTS idx_cadences_room        ON equipment_cadences(room_code);
CREATE INDEX IF NOT EXISTS idx_cadences_product     ON equipment_cadences(product_code);

